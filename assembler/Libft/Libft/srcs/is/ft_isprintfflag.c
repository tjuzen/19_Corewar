#include "../../includes/libft.h"

int	ft_isprintfflag(int c)
{
	if (c == 'c' || c == 'C' || c == 'd' || c == 'D' || c == 'i' || \
	c == 'e' || c == 'E' || c == 'f' || c == 'F' || c == 'g' || c == 'G' || \
	c == 'o' || c == 'O' || c == 's' || c == 'S' || c == 'u' || c == 'U' || \
	c == 'x' || c == 'X' || c == 'p' || c == 'n' || c == 'N' || c == '%' || \
	c == 't' || c == 'T' || c == 'r' || c == 'R')
		return (1);
	return (0);
}
