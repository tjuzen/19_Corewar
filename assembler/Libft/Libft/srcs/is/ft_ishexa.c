#include "../../includes/libft.h"

int		ft_ishexa(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 48 && str[i] <= 57) || \
		(str[i] >= 65 && str[i] <= 90) || \
		(str[i] >= 97 && str[i] <= 122))
			i++;
		else
			return (0);
	}
	return (1);
}
