#include "../../includes/libft.h"

void	ft_putstr_fd_c0(char const *s, int fd, int c0)
{
	int i;

	if (s && fd)
	{
		i = 0;
		while (s[i] != '\0' || c0 != 0)
		{
			if (s[i] == '\0' && c0 != 0)
				c0--;
			ft_putchar_fd(s[i], fd);
			i++;
		}
	}
}
