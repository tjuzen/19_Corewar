#include "../../includes/libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t i;
	size_t j;

	i = ft_strlen(dest);
	j = -1;
	while (++j < n && src[j])
		dest[i + j] = src[j];
	dest[i + j] = '\0';
	return (dest);
}
